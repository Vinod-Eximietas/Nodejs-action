// addNumbers function adds two numbers and returns the result
function addNumbers(a, b) {
    return a + b;
}

// subtractNumbers function subtracts one number from another and returns the result
function subtractNumbers(a, b) {
    return a - b;
}

// Main function to compute the result of adding and subtracting numbers and print the results
function main() {
    const result1 = addNumbers(5, 3);
    const result2 = subtractNumbers(10, 4);
    console.log("Result of adding:", result1);
    console.log("Result of subtracting:", result2);
    //console.log(1);
}

// Call the main function to execute the program
main();
